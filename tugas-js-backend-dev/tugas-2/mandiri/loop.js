// Buat program yang menggunakan loop forEach untuk mencetak angka 1 sampai 10.

console.log("===================FOR===================" )
for (let i = 0; i < 10; i++) {
    console.log("number", i + 1);
}

numbers = [1,2,3,4,5,6,7,9,10]

console.log("=================FOREACH=================" )
numbers.forEach(number => {
    console.log("number", number)
});
